export type PlagiarismPromptParams = {
  content: string;
};

export function buildPlagiarismPrompt(p: PlagiarismPromptParams): string {
  return `${BASE_SYSTEM_PROMPT}

РОЛЬ:
${PLAGIARISM_ROLE}

ТИП ЗАДАНИЯ: проверка на плагиат и AI-генерацию.

ЧТО ДЕЛАТЬ:

Проанализируй текст ниже на:
  1) Признаки AI-генерации (GPT/Claude/Gemini): однородная сложность, шаблонные коннекторы, обтекаемые формулировки, отсутствие личного опыта, чрезмерная "правильность", симметричная структура абзацев, частые перечисления через запятую.
  2) Возможные заимствования: фрагменты в стиле учебников/Википедии, резкие смены стиля, неестественные переходы (признак копипаста), машинно-переведённые конструкции.
  3) Общую характеристику стиля.

Каждый подозрительный фрагмент — прямая цитата (1-3 предложения) + пояснение, почему ты его подозреваешь.

ВЕРДИКТ:
  - "clean" — текст похож на оригинальный, написан человеком; aiProbability < 30, originalityScore > 70.
  - "review" — есть подозрительные фрагменты, но не однозначно (30 ≤ aiProbability ≤ 65).
  - "plagiarism" — высокая вероятность AI-генерации или заимствования (aiProbability > 65 ИЛИ есть явные копипаст-фрагменты).

ТЕКСТ ДЛЯ ПРОВЕРКИ:
"""
${p.content}
"""

ОБЯЗАТЕЛЬНЫЙ ФОРМАТ ОТВЕТА — верни JSON ТОЧНО с такими именами полей:
{
  "originalityScore": 85,
  "aiProbability": 15,
  "suspiciousFragments": [
    { "quote": "дословная цитата из текста", "reason": "пояснение почему подозрительно" }
  ],
  "verdict": "clean",
  "summary": "2-4 предложения общего вывода на русском языке."
}

Правила:
- originalityScore: число 0..100 (100 = полностью оригинальный).
- aiProbability: число 0..100 (100 = точно AI-сгенерирован).
- suspiciousFragments: может быть пустым массивом []. Цитаты — ДОСЛОВНО из текста.
- verdict: строго одно из "clean", "review", "plagiarism".
- summary: только на русском.
- Никаких лишних ключей, никаких обёрток — только этот JSON.
`.trim();
}
