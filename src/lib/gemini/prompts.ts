function getCodeRole(language: string): string {
  return (
    CODE_ROLE_BY_LANGUAGE[language] ??
    `Ты — Senior разработчик с 20+ годами опыта в языке ${language}. Эксперт в идиоматическом коде, читаемости, производительности, безопасности и тестировании на ${language}.`
  );
}

